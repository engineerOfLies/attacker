void LoadLevelSprites()
{
  LevelSprite = LoadSprite("images/spacetext.png",64,64,-1,-1,-1);
  XTilesPerBuffer = S_Data.xres / LevelSprite->w * 2;
  YTilesPerBuffer = S_Data.yres / LevelSprite->h;
}

void RenderLevel(Level *level)
{
    int i,j; //ye olde loop counting variables
    for(i = 0;i < XTilesPerBuffer;i++)
    {
      if(i >= level->length)break; //No need to render whats not really there.
      for(j =0;j < YTilesPerBuffer;j++)
      {
        DrawSprite(LevelSprite,buffer,i * LevelSprite->w, j * LevelSprite->h,level->map[i].Tiles[j]);
      }
      for(j = 0;j < TAGS;j++)
      {
        switch(level->map[i].tags[j])
        {
          case T_Bug:
            SpawnBug(i * LevelSprite->w + rand()%32,j * 190,0);
          break;
          case T_BugA:
            SpawnBug(i * LevelSprite->w + rand()%32,j * 190,1);
          break;
          case T_Mant:
            SpawnMantid(i * LevelSprite->w + rand()%32,j * 190,0);
          break;
          case T_MantA:
            SpawnMantid(i * LevelSprite->w + rand()%32,j * 190,1);
          break;
          case T_POW1:
            DropPowerUp(i * LevelSprite->w,j * 190,-4,0,0);
          break;
          case T_POW2:
            DropPowerUp(i * LevelSprite->w,j * 190,-4,0,1);
          break;
        }
      }
    }
    Abs_Camera.x = 0;
    NextColumn = XTilesPerBuffer / 2;
    if(BgMusic != NULL)
    {
      Mix_HaltMusic();
      Mix_FreeMusic(BgMusic);
    }
    BgMusic = Mix_LoadMUS(level->bgmusic);
    if(BgMusic == NULL)
    {
      fprintf(stderr, "Unable to Load Background Music: %s\n", SDL_GetError());
      exit(1);
    }
    Mix_PlayMusic(BgMusic,-1);/*play my background music infinitely*/
}

void UpdateLevel(Level *level)
{
  int i,j;
  for(i = 0;i < (XTilesPerBuffer / 2);i++)
  {
    for(j =0;j < YTilesPerBuffer;j++)
    {
      DrawSprite(LevelSprite,buffer,(i + NextColumn) * LevelSprite->w, j * LevelSprite->h,level->map[NextColumn + i].Tiles[j]);
    }
    for(j = 0;j < TAGS;j++)
    {
      switch(level->map[i + NextColumn].tags[j])
      {
        case T_Bug:
          SpawnBug((i + (XTilesPerBuffer / 2)) * LevelSprite->w + rand()%32,j * 190,0);
        break;
        case T_BugA:
          SpawnBug((i + (XTilesPerBuffer / 2)) * LevelSprite->w + rand()%32,j * 190,1);
        break;
          case T_Mant:
            SpawnMantid(i * LevelSprite->w + rand()%32,j * 190,0);
          break;
          case T_MantA:
            SpawnMantid(i * LevelSprite->w + rand()%32,j * 190,1);
          break;
        case T_POW1:
          DropPowerUp((i + (XTilesPerBuffer / 2)) * LevelSprite->w,j * 190,-4,0,0);
        break;
        case T_POW2:
          DropPowerUp((i + (XTilesPerBuffer / 2)) * LevelSprite->w,j * 190,-4,0,1);
        break;
      }
    }
  }
  NextColumn += i;
}

Level *GetCurrentLevel(int level)
{
  return &TestLevel[level];
}

int MoveCamera(int Length)
{
  Level *level;
  level = GetCurrentLevel(CurrentLevel);
  if(Abs_Camera.x + Camera.w >= LevelSprite->w * level->length)return 0;//we are done already.
  Abs_Camera.x += Length;
  Camera.x += Length;
  if(Camera.x > Camera.w)
  {
    SDL_BlitSurface(buffer,&Camera,buffer,NULL);
    UpdateLevel(level);
    Camera.x = 0;
  }
  return 1;
}



